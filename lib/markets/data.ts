const round = (value: number, places = 2): number => {
  const factor = 10 ** places
  return Math.round(value * factor) / factor
}

interface HistoryOptions {
  start: string
  yesProbabilities: readonly number[]
  baseLiquidity: number
  baseVolume: number
  liquidityStep?: number
  volumeStep?: number
  cadenceDays?: number
}

const buildHistory = ({
  start,
  yesProbabilities,
  baseLiquidity,
  baseVolume,
  liquidityStep = 18,
  volumeStep = 31,
  cadenceDays = 14,
}: HistoryOptions): MarketHistoryPoint[] => {
  const startTime = Date.parse(start)

  return yesProbabilities.map((yesProbability, index) => {
    const totalLiquidity = baseLiquidity + liquidityStep * index
    const yesLiquidity = round(totalLiquidity * yesProbability)
    const noLiquidity = round(totalLiquidity - yesLiquidity)

    return {
      timestamp: new Date(
        startTime + index * cadenceDays * 86_400_000,
      ).toISOString(),
      yesProbability,
      noProbability: round(1 - yesProbability, 4),
      totalVolume: round(baseVolume + volumeStep * index),
      yesLiquidity,
      noLiquidity,
      dataLabel: "SAMPLE DATA",
    }
  })
}

